package Java00Tutorial.Java004Output;

public class JavaOutput {

    public static void main(String[] args) {
        //Print text and a new line
        System.out.println("Hello, there. Here is some text with a new line at the end.");
        //Print text without a new line
        System.out.print("Hello, there. Here is some text without a new line at the end.");
        //Print a number
        System.out.println(5);
        //Print some mathematical calculations
        System.out.println(5+5);
        System.out.println(5*5);
    }
}
